public class Ex2_1{     // 定義public類別Ex2_1
    public static void main(String[] args){     // main()函數，主程式開始
        int n;  // 宣告整數變數n
        n=8;    // 為變數n設值為8
        System.out.println(n+"+"+n+"="+(n+n));  // 印出n+n的值
        System.out.println(n+"*5="+(n*5));      // 印出n*5的值
    }
}

/* output---
8+8=16
8*5=40 
----------*/