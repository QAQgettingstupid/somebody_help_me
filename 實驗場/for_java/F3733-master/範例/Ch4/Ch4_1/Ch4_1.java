// Ch4_1, 算術運算子的使用
public class Ch4_1{
    public static void main(String[] args){
        int a=9, b=5;

        System.out.printf("%d + %d=%d\n",a,b,a+b);    // 相加
        System.out.printf("%d - %d=%d\n",a,b,a-b);    // 相減
        System.out.printf("%d * %d=%d\n",a,b,a*b);    // 相乘
        System.out.printf("%d / %d=%d\n",a,b,a/b);    // 相除
        System.out.printf("%d %% %d=%d\n",a,b,a%b);   // 取餘數
    }
}